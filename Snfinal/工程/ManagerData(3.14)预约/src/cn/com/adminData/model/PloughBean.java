package cn.com.adminData.model;

public class PloughBean {
	private int ID;
	private int ExtendedId;
	private String SoilTexture;
	private String SoilCondition;
	private String Vestigital;
	private String Location;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getExtendedId() {
		return ExtendedId;
	}
	public void setExtendedId(int extendedId) {
		ExtendedId = extendedId;
	}
	public String getSoilTexture() {
		return SoilTexture;
	}
	public void setSoilTexture(String soilTexture) {
		SoilTexture = soilTexture;
	}
	public String getSoilCondition() {
		return SoilCondition;
	}
	public void setSoilCondition(String soilCondition) {
		SoilCondition = soilCondition;
	}
	public String getVestigital() {
		return Vestigital;
	}
	public void setVestigital(String vestigital) {
		Vestigital = vestigital;
	}
	public String getLocation() {
		return Location;
	}
	public void setLocation(String location) {
		Location = location;
	}
}
