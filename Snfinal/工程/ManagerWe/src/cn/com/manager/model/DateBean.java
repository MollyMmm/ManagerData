package cn.com.manager.model;

public class DateBean {

	// 日期的id
	private int date_id;
	// 日期名字
	private String date_name;
	// 日期的数目
	private int date_num;
	public int getDate_num() {
		return date_num;
	}

	public void setDate_num(int dateNum) {
		date_num = dateNum;
	}


	public String getDate_name() {
		return date_name;
	}

	public void setDate_name(String dateName) {
		date_name = dateName;
	}

	public int getDate_id() {
		return date_id;
	}

	public void setDate_id(int dateId) {
		date_id = dateId;
	}

}
